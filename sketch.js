//Estrutura do jogo da velha, que será preencida conforme os jogadores jogam
let board = [
  ['', '', ''],
  ['', '', ''],
  ['', '', '']
];

let w; // = width / 3;
let h; // = height / 3;

//Jogadores
let ai = 'X';
let human = 'O';
let currentPlayer = human; //Jogador atual

// Criação da tela
function setup() {
  createCanvas(400, 400);
  w = width / 3; //Largura da tela dividida por 3
  h = height / 3; //Altura da tela dividida por 3
  bestMove();
}

//
function equals3(a, b, c) {
  return a == b && b == c && a != '';
}


// Verifica se há vencedor
function checkWinner() {
  let winner = null;

  // verifica se há vencedor na horizontal
  for (let i = 0; i < 3; i++) {
    if (equals3(board[i][0], board[i][1], board[i][2])) {
      winner = board[i][0];
    }
  }

  // verifica se há vencedor vertical
  for (let i = 0; i < 3; i++) {
    if (equals3(board[0][i], board[1][i], board[2][i])) {
      winner = board[0][i];
    }
  }

  // verifica se há vencedor diagonal
  if (equals3(board[0][0], board[1][1], board[2][2])) {
    winner = board[0][0];
  }
  if (equals3(board[2][0], board[1][1], board[0][2])) {
    winner = board[2][0];
  }

  let openSpots = 0;
  for (let i = 0; i < 3; i++) {
    for (let j = 0; j < 3; j++) {
      if (board[i][j] == '') {
        openSpots++;
      }
    }
  }

  // Verifica se empatou
  if (winner == null && openSpots == 0) {
    return 'tie';
  } else {
    return winner;
  }
}

function mousePressed() {
  if (currentPlayer == human) {
    // Human make turn
    let i = floor(mouseX / w);
    let j = floor(mouseY / h);
    // If valid turn
    if (board[i][j] == '') {
      board[i][j] = human;
      currentPlayer = ai;
      bestMove();
    }
  }
}

// Desenha linhas, colunas, X e O 
function draw() {
  background(255);
  strokeWeight(4);

  // Desenho de linhas e colunas
  line(w, 0, w, height);
  line(w * 2, 0, w * 2, height);
  line(0, h, width, h);
  line(0, h * 2, width, h * 2);

  // Desenho de X e O
  for (let j = 0; j < 3; j++) {
    for (let i = 0; i < 3; i++) {
      let x = w * i + w / 2;
      let y = h * j + h / 2;
      let spot = board[i][j];
      textSize(32);
      let r = w / 4;
      if (spot == human) {
        noFill();
        ellipse(x, y, r * 2);
      } else if (spot == ai) {
        line(x - r, y - r, x + r, y + r);
        line(x + r, y - r, x - r, y + r);
      }
    }
  }

  // Ganhou, empatou ou perdeu
  let result = checkWinner();
  if (result != null) {
    noLoop();
    let resultP = createP('');
    resultP.style('font-size', '32pt');
    if (result == 'tie') {
      resultP.html('Empatou!');
    } else {
      resultP.html(`${result} ganhou!`);
    }
  }
}